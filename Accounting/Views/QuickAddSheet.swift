                    ? Color.gray.opacity(0.3)
                    : selectedCategory.color
            )
            .clipShape(RoundedRectangle(cornerRadius: 16))
        }
        .disabled(isCalculatorMode ? calculatedAmount <= 0 : amount.isEmpty)
        .padding(.horizontal, 20)
    }
}

